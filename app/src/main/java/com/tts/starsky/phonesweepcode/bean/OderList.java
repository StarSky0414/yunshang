package com.tts.starsky.phonesweepcode.bean;

/**
 *  主页面订单使用
 */
public class OderList {

    // 商品名称
    private String goodName;

    // 销售时间
    private String oderTime;

    // 商品图片路径
    private String shopPhoto;

    // 盈利多少
    private String mProfit;

    public String getGoodName() {
        return goodName;
    }

    public void setGoodName(String goodName) {
        this.goodName = goodName;
    }

    public String getOderTime() {
        return oderTime;
    }

    public void setOderTime(String oderTime) {
        this.oderTime = oderTime;
    }

    public String getShopPhoto() {
        return shopPhoto;
    }

    public void setShopPhoto(String shopPhoto) {
        this.shopPhoto = shopPhoto;
    }

    public String getmProfit() {
        return mProfit;
    }

    public void setmProfit(String mProfit) {
        this.mProfit = mProfit;
    }
}
